                <h1>Lekkage Tips: Voorkomen en Oplossen</h1>
                <p class="blog-meta">Geplaatst op 6 juli 2025 door Loodgieter Paraat</p>
                <img src="./images/lekkage.jpg" alt="Afbeelding bij lekkage tips" class="blog-post-image">
