        <h2>II. Doeleinden van de verwerking</h2>
        <p>
            Uw persoonsgegevens worden verzameld en verwerkt voor de volgende doeleinden:
        </p>
        <ul>
            <li>Het opnemen van contact en beantwoorden van uw vragen</li>
            <li>Het uitvoeren van onze loodgietersdiensten</li>
            <li>Het opstellen van offertes en facturen</li>
            <li>Het voldoen aan wettelijke verplichtingen (zoals administratieplicht)</li>
            <li>Het verbeteren van onze dienstverlening en klantenservice</li>
        </ul>

        <h2>III. Bewaartermijn</h2>
        <p>
            Uw persoonsgegevens worden niet langer bewaard dan nodig is voor de doeleinden waarvoor ze zijn verzameld. Wij hanteren de volgende bewaartermijnen:
        </p>
        <ul>
            <li>Contactgegevens en correspondentie: maximaal 2 jaar na het laatste contact</li>
            <li>Facturen en administratieve gegevens: 7 jaar (wettelijke bewaarplicht)</li>
            <li>Offertes: 1 jaar na datum offerte</li>
        </ul>

        <h2>IV. Delen van gegevens</h2>
        <p>
            Loodgietersbedrijf Paraat deelt uw gegevens niet met derden, tenzij dit noodzakelijk is voor de uitvoering van onze diensten of wanneer wij hiertoe wettelijk verplicht zijn. Met partijen die in onze opdracht gegevens verwerken, sluiten wij een verwerkersovereenkomst.
        </p>

        <h2>V. Beveiliging van persoonsgegevens</h2>
        <p>
            Wij nemen passende technische en organisatorische maatregelen om uw persoonsgegevens te beschermen tegen verlies, misbruik, onbevoegde toegang of openbaarmaking.
        </p>

        <h2>VI. Uw rechten</h2>
        <p>
            U heeft de volgende rechten met betrekking tot uw persoonsgegevens:
        </p>
        <ul>
            <li><strong>Recht op inzage:</strong> U mag vragen welke persoonsgegevens wij van u verwerken.</li>
            <li><strong>Recht op rectificatie:</strong> U mag ons verzoeken om onjuiste gegevens te corrigeren.</li>
            <li><strong>Recht op verwijdering:</strong> U mag ons verzoeken om uw persoonsgegevens te verwijderen.</li>
            <li><strong>Recht op bezwaar:</strong> U mag bezwaar maken tegen de verwerking van uw gegevens.</li>
            <li><strong>Recht op gegevensoverdraagbaarheid:</strong> U mag uw gegevens in een gestructureerd, gangbaar en machineleesbaar formaat ontvangen.</li>
        </ul>

