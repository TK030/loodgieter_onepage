        <h2>I. Toepasselijkheid</h2>
        <p>
            Deze algemene voorwaarden zijn van toepassing op alle offertes, werkzaamheden, overeenkomsten en leveringen van diensten of goederen door of namens Loodgietersbedrijf Paraat.
        </p>

        <h2>II. Offertes en overeenkomsten</h2>
        <ul>
            <li>Alle offertes van Loodgietersbedrijf Paraat zijn vrijblijvend, tenzij uitdrukkelijk anders vermeld.</li>
            <li>Een overeenkomst komt tot stand na schriftelijke of mondelinge bevestiging door Loodgietersbedrijf Paraat.</li>
        </ul>

        <h2>III. Uitvoering van de werkzaamheden</h2>
        <ul>
            <li>Loodgietersbedrijf Paraat zal de werkzaamheden naar beste inzicht en vermogen uitvoeren.</li>
            <li>Indien en voor zover een goede uitvoering van de overeenkomst dit vereist, heeft Loodgietersbedrijf Paraat het recht bepaalde werkzaamheden te laten verrichten door derden.</li>
        </ul>

        <h2>IV. Prijzen en betalingen</h2>
        <ul>
            <li>Alle prijzen zijn in euro’s en exclusief btw, tenzij anders vermeld.</li>
            <li>Betaling dient te geschieden binnen 14 dagen na factuurdatum, tenzij schriftelijk anders is overeengekomen.</li>
            <li>Bij niet-tijdige betaling is de klant van rechtswege in verzuim en is Loodgietersbedrijf Paraat gerechtigd wettelijke rente en incassokosten in rekening te brengen.</li>
        </ul>

        <h2>V. Aansprakelijkheid</h2>
        <ul>
            <li>Loodgietersbedrijf Paraat is niet aansprakelijk voor schade, van welke aard ook, ontstaan doordat is uitgegaan van door of namens de klant verstrekte onjuiste en/of onvolledige gegevens.</li>
            <li>De aansprakelijkheid van Loodgietersbedrijf Paraat is beperkt tot het bedrag dat in het betreffende geval door de aansprakelijkheidsverzekering wordt uitgekeerd.</li>
        </ul>

        <h2>VI. Klachten</h2>
        <ul>
            <li>Klachten over de verrichte werkzaamheden dienen binnen 8 dagen na ontdekking schriftelijk te worden gemeld.</li>
            <li>Indien een klacht gegrond is, zal Loodgietersbedrijf Paraat de werkzaamheden alsnog verrichten zoals overeengekomen.</li>
        </ul>

        <h2>VII. Overmacht</h2>
        <p>
            Loodgietersbedrijf Paraat is niet gehouden tot het nakomen van enige verplichting indien zij daartoe gehinderd wordt als gevolg van overmacht.
        </p>

        <h2>VIII. Toepasselijk recht en geschillen</h2>
        <p>
            Op alle rechtsverhoudingen tussen Loodgietersbedrijf Paraat en de klant is uitsluitend Nederlands recht van toepassing. Geschillen worden voorgelegd aan de bevoegde rechter in het arrondissement waar Loodgietersbedrijf Paraat is gevestigd.
        </p>

        <h2>IX. Wijzigingen algemene voorwaarden</h2>
        <p>
            Loodgietersbedrijf Paraat behoudt zich het recht voor deze algemene voorwaarden te wijzigen. Wij raden u aan deze pagina regelmatig te raadplegen voor de meest actuele versie.
