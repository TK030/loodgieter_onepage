            <section class="blog-post-content">
                <p>Een lekkende keukenkraan of een slecht functionerende afvoer kan veel ongemak veroorzaken. Gelukkig is het vervangen van een keukenkraan of sifon vaak goed te doen – mits het correct gebeurt. Loodgieter Paraat deelt 5 belangrijke tips om schade en lekkage te voorkomen.</p>

                <h2>1. Kies de juiste kraan of sifon</h2>
                <p>Niet elke kraan of afvoer past in elke keuken. Controleer of de aansluitingen overeenkomen met uw situatie. Voor kranen geldt dat er verschillende types zijn (mengkraan, eenhendel of kokendwaterkraan). Kijk vooral naar kwaliteit, want een goedkope kraan gaat vaak minder lang mee.</p>

                <h2>2. Water afsluiten en voorbereidende stappen</h2>
                <p>Sluit altijd de watertoevoer af voordat u begint. Zet de hoofdkraan of de kraan onder de spoelbak dicht. Houd daarnaast handdoeken en een emmer klaar voor eventueel restwater.</p>

                <h2>3. Oude kraan of afvoer demonteren</h2>
                <p>Verwijder voorzichtig de oude onderdelen. Gebruik hiervoor passend gereedschap om beschadigingen aan leidingen en aansluitingen te voorkomen. Noteer hoe alles is aangesloten, zodat u dit later correct terug kunt plaatsen.</p>

                <h2>4. Nieuwe onderdelen monteren</h2>
                <p>Monteer de nieuwe keukenkraan of sifon stap voor stap volgens de handleiding. Let op dat alle rubbers en ringen goed geplaatst zijn en gebruik installatiesleutels met beleid om beschadiging te voorkomen.</p>

                <h2>5. Lekkages controleren</h2>
                <p>Zet het water weer aan en controleer alle aansluitingen zorgvuldig. Laat de kraan enige tijd lopen en kijk of er druppels onder de spoelbak verschijnen. Zo voorkomt u verrassingen achteraf.</p>

                <p><strong>Conclusie:</strong> Het vervangen van een keukenkraan of afvoer lijkt eenvoudig, maar kan tricky zijn. Wilt u helemaal zeker zijn van een waterdichte aansluiting? <a href="./index.php#contact">Neem contact op met Loodgieter Paraat</a> voor professionele montage in Utrecht en omgeving.</p>
